#include "os/os.h"
#include <base.h>

/**
 * Generates a 64-bit random number.
 *
 * if rand is NULL, then LIBSPDM_ASSERT().
 *
 * @param[out] rand_data     buffer pointer to store the 64-bit random value.
 *
 * @retval true         Random number generated successfully.
 * @retval false        Failed to generate the random number.
 *
 **/
bool libspdm_get_random_number_64(uint64_t *rand_data)
{
    NV_STATUS status = NV_ERR_GENERIC;

    if (rand_data == NULL)
    {
        return false;
    }
    status = osGetRandomBytes((uint8_t *)rand_data, sizeof(uint64_t));
    return (status == NV_OK);
}
